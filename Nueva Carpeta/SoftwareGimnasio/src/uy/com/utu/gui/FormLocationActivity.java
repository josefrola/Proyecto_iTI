    public final void loadConfigurationGym() {

        String[] title = {"Codigo", "Nombre", "Dirección", "Teléfono"};
        modelo = new DefaultTableModel(null, title);
        String[] fila = new String[5];
        list = ConGymBus.list();

        for (ConfigurationGim c : list) {
            fila[0] = String.valueOf(c.getIdconfiguration());
            fila[1] = c.getNameGim();
            fila[2] = c.getAddress();
            fila[3] = c.getPhone();
            modelo.addRow(fila);
        }
        tbConfigurationGym.setModel(modelo);

    }

    public void asingConfigurationGym() {
        configurationGim = new ConfigurationGim();

        configurationGim.setIdconfiguration(Integer.parseInt(txtcodigo.getText()));
        configurationGim.setNameGim(txtgimnasio.getText());
        configurationGim.setAddress(txtaddress.getText());
        configurationGim.setPhone(txtphone.getText());

    }

    public void insertConfigurationGym() {
        String rpta;
        asingConfigurationGym();
        rpta = ConGymBus.insertConfigurationGym(configurationGim);
        if (rpta == null) {
            JOptionPane.showMessageDialog(null, "Se Ingreso el nuevo Gimnasio Correctamente");
        } else {
            JOptionPane.showMessageDialog(null, "Error al ingresar los datos");
        }
    }

    public void codConfigurationGym() {
        Integer id = ConGymBus.idConfigurationGym();
        txtcodigo.setText(String.valueOf(id));
    }

    public void searConfigurationGym(Integer id) {
        configurationGim = ConGymBus.get(id);
        if (configurationGim.getIdconfiguration() != null) {
            txtcodigo.setText(String.valueOf(configurationGim.getIdconfiguration()));
            txtgimnasio.setText(configurationGim.getNameGim());
            txtaddress.setText(configurationGim.getAddress());
            txtphone.setText(configurationGim.getPhone());
        } else {
            JOptionPane.showMessageDialog(null, "Gimnasio no Encontrado");
        }
    }

    public void deleteConfigurationGym() {
        Integer id = Integer.parseInt(txtcodigo.getText());
        String rpta = ConGymBus.delete(id);
        if (rpta == null) {
            JOptionPane.showMessageDialog(null, "Se eliminaron correctamente los datos ");
        } else {
            JOptionPane.showMessageDialog(null, "Error al eliminar los datos" + rpta);
        }
    }

    public void updateConfigurationGym() {
        asingConfigurationGym();
        String rpta = ConGymBus.update(configurationGim);
        if (null != rpta) {
            JOptionPane.showMessageDialog(null, "Error al Actualizar los datos");
        } else {
            JOptionPane.showMessageDialog(null, "Se Actualizarón los datos correctamente");
        }
    }

    public void clearEntry() {
        txtcodigo.setText(null);
        txtgimnasio.setText(null);
        txtaddress.setText(null);
        txtphone.setText(null);
    }

