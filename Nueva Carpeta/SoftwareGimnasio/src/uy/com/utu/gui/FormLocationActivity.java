    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        try {
            if ((txtcodigo.getText().trim().isEmpty())&&(txtgimnasio.getText().trim().isEmpty())&&(txtaddress.getText().trim().isEmpty())&&(txtphone.getText().trim().isEmpty())) {
                if (option == true) {
                    insertConfigurationGym();
                } else {
                    updateConfigurationGym();
                }
            } else {
                JOptionPane.showMessageDialog(null, "Los Campos no pueden ser nulos");
            }

            loadConfigurationGym();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error en el sistema" + e.getMessage());
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

