    private void loadtablelist() {
       String[] titles={"Plan","Hora Comienzo", "Hora Fin","Lunes","Martes","Miercoles","Juves","Viernes","Sabado","Precio"};
       modelo = new DefaultTableModel(null, titles);
       String[] row = new String[11];
       list=actconfbus.list();
       
       for(ConfigurationActivity conf : list){
           row[0]=String.valueOf(conf.getNameconf());
           row[1]=String.valueOf(conf.getFrom_time());
           row[2]=String.valueOf(conf.getTime_to());
           row[3]=String.valueOf(conf.getMonday());
           row[4]=String.valueOf(conf.getTuesday());
           row[5]=String.valueOf(conf.getWednsesday());
           row[6]=String.valueOf(conf.getThursday());
           row[7]=String.valueOf(conf.getFriday());
           row[8]=String.valueOf(conf.getSaterday());
           row[9]=String.valueOf(conf.getPrice());
           modelo.addRow(row);
       }
       tablelistact.setModel(modelo);
    }
