    private Integer ReturnIdplan(String selecionado) throws SQLException {
        ConectaDB con = new ConectaDB();
        Connection conn = con.getConnection();
        ResultSet rs;
        String sql = "SELECT ID FROM CONFIGURATION_ACTIVITY WHERE NAME=?";
        PreparedStatement sqls = conn.prepareStatement(sql);
        sqls.setString(1, selecionado);
        rs = sqls.executeQuery();
        Integer ret_id = 0;
        while (rs.next()) {
            ret_id = rs.getInt("ID");
        }
        return ret_id;
    }

    private Integer ReturnIdSexo(String selecionoIdSexo) throws SQLException {
        ConectaDB con = new ConectaDB();
        Connection conn = con.getConnection();
        ResultSet rs;

        String sql = "SELECT ID_SEXO FROM SEXO WHERE NAME_SEXO=?";
        PreparedStatement sqls = conn.prepareStatement(sql);
        sqls.setString(1, selecionoIdSexo);
        rs = sqls.executeQuery();
        Integer idSexo = 0;
        while (rs.next()) {
            idSexo = rs.getInt("ID_SEXO");
        }

        return idSexo;

    }
