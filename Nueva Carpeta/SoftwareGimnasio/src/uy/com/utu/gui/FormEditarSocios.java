    private final void caragarSocios() {
       try{
        String[] title = {"Nombre", "Apellido", "Dirección", "Teléfono", "Cel"};
        modelo = new DefaultTableModel(null, title);
        String[] row = new String[5];
        list = sociosBus.list();
        for (SociosAll s : list) {
            row[0] = String.valueOf(s.getNameSocio());
            row[1] = String.valueOf(s.getLastNameSocio());
            row[2] = String.valueOf(s.getAddress());
            row[3] = String.valueOf(s.getPhone());
            row[4] = String.valueOf(s.getMovile());
            modelo.addRow(row);

        }

        tablesocios.setModel(modelo);
       }catch(Exception e){
           
       }
    }

    public void asingnarSocios() {

        sociosall = new SociosAll();

        String selecionadoplan = (String) comboboxplan.getSelectedItem();
        String seleccionarsexo = (String) comboboxsexo.getSelectedItem();
        String funcionario = (String) combofuncionario.getSelectedItem();
        String participa = (String) comboactividades.getSelectedItem();

        Integer idplan = 0;
        Integer idsexo = 0;

        try {

            idplan = ReturnIdplan(selecionadoplan);
            idsexo = ReturnIdSexo(seleccionarsexo);

            sociosall.setNameSocio(txtnombre.getText());
            sociosall.setLastNameSocio(txtapellido.getText());
            sociosall.setAge(Integer.parseInt(txtfechanacimiento.getText()));
            sociosall.setCI(txtci.getText());
            sociosall.setIdSexo(idsexo);
            sociosall.setAddress(txtdireccion.getText());
            sociosall.setPhone(txttel.getText());
            sociosall.setMovile(txtmovil.getText());
            sociosall.setType(funcionario);
            sociosall.setIdPlan(idplan);
            sociosall.setActivity(participa);

        } catch (SQLException ex) {
            Logger.getLogger(ActivitiesTimetable.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

    public void insertSocios() {
        String rpta;
        asingnarSocios();
        rpta = sociosBus.insertSociosall(sociosall);
        if (rpta == null) {
            JOptionPane.showMessageDialog(null, "Se Ingresaron los datos correctamente");
        } else {
            JOptionPane.showMessageDialog(null, "Error al ingresar los datos");
        }
    }

    public void codSocios() {
        Integer id = sociosBus.idSociosall();
        txtidsocio.setText(String.valueOf(id));
    }

    private void buscarSocios(Integer id) {
        try{
        sociosall = sociosBus.get(id);
        if (sociosall.getIdSocio() != null) {
            txtidsocio.setText(String.valueOf(sociosall.getIdSocio()));
            txtnombre.setText(sociosall.getNameSocio());
            txtapellido.setText(sociosall.getLastNameSocio());
            txtfechanacimiento.setText(String.valueOf(sociosall.getAge()));
            txtci.setText(sociosall.getCI());
            txtdireccion.setText(sociosall.getAddress());
            txttel.setText(sociosall.getPhone());
            txtmovil.setText(sociosall.getMovile());

        } else {
            JOptionPane.showMessageDialog(null, "Error al cargar los datos");
        }
        }catch(Exception e){
          JOptionPane.showMessageDialog(null, "Error al cargar los datos" + e.getMessage());
        }
    }

    public void EliminarSocios() {
        try {
            Integer id = Integer.parseInt(txtidsocio.getText());
            String rpta = sociosBus.delete(id);
            if (rpta == null) {
                JOptionPane.showMessageDialog(null, "Datos eliminados correctamente");
            } else {
                JOptionPane.showMessageDialog(null, "Error al eliminar los datos " + rpta);
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al eliminar los datos " + e);
        }

    }

    public void ActualizarSocios() {
        try {
            asingnarSocios();
            String rpta = sociosBus.update(sociosall);
            if (null != rpta) {
                JOptionPane.showMessageDialog(null, "Error al actualizar los datos" + rpta);
            } else {
                JOptionPane.showMessageDialog(null, "Datos Actualizados corerctamente");
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al actualizar los datos " + e);
        }
    }
    
