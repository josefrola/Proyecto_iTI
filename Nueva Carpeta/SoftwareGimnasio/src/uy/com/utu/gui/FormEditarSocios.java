    private void LoadSelectPlan() throws SQLException {
        ConectaDB con = new ConectaDB();
        Connection conn = con.getConnection();
        ResultSet rs;
        String sql = "SELECT NAME FROM CONFIGURATION_ACTIVITY";
        PreparedStatement sqls = conn.prepareStatement(sql);
        rs = sqls.executeQuery();
        while (rs.next()) {
            this.comboboxplan.addItem(rs.getString("NAME"));
        }
    }

